Customer/OM/AR :

1. --Build a query for Active Customer List
SELECT DISTINCT HZP.PARTY_NAME,HZCA.STATUS FROM HZ_PARTIES HZP,HZ_CUST_ACCOUNTS HZCA
WHERE HZP.PARTY_ID = HZCA.PARTY_ID
AND HZCA.STATUS = 'A'

2. --List the Customer those are belongs to Country ‘US’
SELECT DISTINCT HZP.PARTY_NAME,HZP.COUNTRY 
FROM HZ_PARTIES HZP,HZ_CUST_ACCOUNTS HZCA
WHERE HZP.PARTY_ID = HZCA.PARTY_ID
AND COUNTRY = 'US'

3. --Build a query for Customer those who are Inactivated during last 6 months
SELECT HZP.PARTY_NAME,HZCAS.END_DATE AS END_DATE 
FROM HZ_PARTIES HZP,HZ_PARTY_SITES HZPS,HZ_CUST_ACCT_SITES HZCAS 
WHERE HZP.PARTY_ID = HZPS.PARTY_ID 
AND HZPS.PARTY_SITE_ID = HZCAS.PARTY_SITE_ID 
AND HZCAS.END_DATE < SYSDATE 
AND HZCAS.END_DATE > ADD_MONTHS(SYSDATE, -300)

4. --Write a query for active customer Count based on the business Unit.
SELECT HZP.PARTY_NAME AS CUSTOMER,RACTL.SALES_ORDER AS ORDER_NUM,COUNT(RACT.CUSTOMER_TRX_ID) AS AR_INVOICE_COUNT 
FROM HZ_PARTIES HZP,HZ_CUST_ACCOUNTS HZCA,RA_CUSTOMER_TRX_ALL RACT,RA_CUSTOMER_TRX_LINES_ALL RACTL 
WHERE HZP.PARTY_ID = HZCA.PARTY_ID 
AND HZCA.CUST_ACCOUNT_ID = RACT.SOLD_TO_CUSTOMER_ID 
AND RACT.CUSTOMER_TRX_ID = RACTL.CUSTOMER_TRX_ID 
GROUP BY HZP.PARTY_NAME,RACTL.SALES_ORDER

