4. --Build a Query for the Supplier Type other than ‘SUPPLIER’
SELECT VENDOR_TYPE_LOOKUP_CODE FROM POZ_SUPPLIERS
WHERE VENDOR_TYPE_LOOKUP_CODE NOT IN ('SUPPLIER')

5. --Build a Query for Suppliers those are not having Supplier Site
SELECT VENDOR_NAME 
FROM POZ_SUPPLIERS_V
WHERE VENDOR_ID IN 
(SELECT VENDOR_ID 
FROM POZ_SUPPLIERS_V
MINUS
SELECT VENDOR_ID 
FROM POZ_SUPPLIER_SITES_V)

6. --Build a Query for Supplier contact
SELECT DISTINCT VENDOR_NAME,ADDRESS_LINE1,COUNTRY,CITY,ZIP 
FROM POZ_SUPPLIER_SITES_V POSS,POZ_SUPPLIERS_V POS 
WHERE POSS.VENDOR_ID(+) = POS.VENDOR_ID

7. --Write a Query for Supplier and its site List those are assigned to Vision Corporation Business Unit
SELECT DISTINCT NAME,VENDOR_NAME,PARTY_SITE_NAME
FROM HR_ALL_ORGANIZATION_UNITS HRO,POZ_SUPPLIERS_V POS,HZ_PARTY_SITES HZP
WHERE HRO.OBJECT_VERSION_NUMBER = POS.OBJECT_VERSION_NUMBER
AND HZP.PARTY_ID = POS.PARTY_ID
AND NAME = 'Vision Corporation'

8. --Write a query for the supplier’s having Purchase order value more than 1000$
SELECT  VENDOR_NAME,SUM(UNIT_PRICE * QUANTITY) AS TOTAL_AMOUNT
FROM PO_LINES_ALL POL,PO_HEADERS_ALL POH,POZ_SUPPLIERS_V POS 
WHERE POL.PO_HEADER_ID = POH.PO_HEADER_ID
AND POS.VENDOR_ID = POH.VENDOR_ID
GROUP BY VENDOR_NAME
HAVING SUM(UNIT_PRICE * QUANTITY) >= 1000


9. --List Supplier and its open PO counts
SELECT VENDOR_NAME,COUNT(DOCUMENT_STATUS) AS OPEN_COUNT
FROM PO_HEADERS_ALL POH,POZ_SUPPLIERS_V POS
WHERE POH.VENDOR_ID = POS.VENDOR_ID 
AND DOCUMENT_STATUS = 'OPEN'
GROUP BY VENDOR_NAME 

10. --List of Partially Paid invoices with Supplier Name(Supplier, Business Unit , Invoice Number, Open Invoice Amount)
SELECT 
HRO.NAME AS BUINESS_UNIT,API.INVOICE_NUM AS INVOICE_NUMBER,
(INVOICE_AMOUNT-AMOUNT_PAID) AS OPEN_INVOICE_AMT,POS.VENDOR_NAME AS SUPPLIER
FROM
HR_ALL_ORGANIZATION_UNITS HRO,
AP_INVOICES_ALL API,
POZ_SUPPLIERS_V POS
WHERE 
(INVOICE_AMOUNT-AMOUNT_PAID) > 0
AND
API.ORG_ID=HRO.ORGANIZATION_ID
AND 
HRO.OBJECT_VERSION_NUMBER=POS.OBJECT_VERSION_NUMBER

